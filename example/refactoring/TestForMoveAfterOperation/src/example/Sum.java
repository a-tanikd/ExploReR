package example;

public class Sum {

  public int sum() {
    int[] array = {0, 1, 2, 3, 4, 5};

    int a;
    int b;
    int c;

    for (int i = 0; i < array.length; ++i) {
      int x;
      int y;

      if (array.length == 0) {
        continue;
      }
      x = 10;
      y = 2 * x;
    }

    a = 1;
    b = 2;
    c = 3;

    return a + b + c;
  }
}
